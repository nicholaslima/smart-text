class Jogador {
    constructor() {
        this._texto = texto;
        this._acertos = acertos;
        this._erros = erros;
        this._tempo = tempo;
    }
    get texto() {
        return this._texto;
    }
    get acertos() {
        return this._acertos;
    }
    get erros() {
        return this._erros;
    }
    get tempo() {
        return this._tempo;
    }
}
